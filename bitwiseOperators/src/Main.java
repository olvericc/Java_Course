public class Main {

    public static void main(String[] args) {

        // variables declaration
        int number1 = 89;
        int number2 = 60;

        // learning about bitwise operators

        // operator and
        System.out.println(number1 & number2);

        // operator or
        System.out.println(number1 | number2);

        //operator xor
        System.out.println(number1 ^ number2);

    }
}