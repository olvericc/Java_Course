public class Main {
    public static void main(String[] args) {

        // progressive count
        for (int i = 0; i < 5; i++) {
            System.out.println("Value of i: " + i);
        }
        System.out.println();

        // regressive count
        for (int j = 4; j >= 0; j--) {
            System.out.println("Value of j: " + j);
        }
    }
}